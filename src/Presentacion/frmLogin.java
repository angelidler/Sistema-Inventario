    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        // TODO add your handling code here:

        String pass = new String(txtPassword.getText());
        String usuario = txtUsuario.getText();

        DUsuarios us = new DUsuarios();
        LUsuario ul = new LUsuario();
        us.setUsuario(usuario);
        us.setClaveUs(pass);
        String Perfil = ul.setPerfil(us);
        
        if (Perfil.equals("")) {

            JOptionPane.showMessageDialog(null, "Este usuario no se encuentra en el sistemar consulte con el administrador", "Informacion", JOptionPane.INFORMATION_MESSAGE);
            txtUsuario.setText("");
            txtPassword.setText("");
            txtUsuario.requestFocusInWindow();
        } else {
            MCPrincipal principal = new MCPrincipal();
            principal.setExtendedState(JFrame.MAXIMIZED_BOTH);
            principal.setVisible(true);
            principal.setDatos(usuario, Perfil);
            this.dispose();
        }

    }//GEN-LAST:event_btnEntrarActionPerformed

